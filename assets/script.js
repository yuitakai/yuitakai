/* ============================================================
   Physically inspired water refraction ripple v4

   Goal:
   - No white rings.
   - No object appearing/disappearing.
   - No page-wide movement.
   - The visible page is captured to an offscreen canvas, then only the
     wave region is re-sampled with a radial displacement field.
   - Grey shading is added from the wave slope to express concave/convex relief.

   This uses html2canvas when available. If the CDN fails, it still draws
   subtle grey wave shadows, but true text/image refraction needs the capture.
   ============================================================ */

const waterCanvas = document.getElementById("waterRefraction");
const waterCtx = waterCanvas?.getContext("2d", { alpha: true, willReadFrequently: true });

const waterSettings = {
  // Keep this near 1.0 for stable performance on GitHub Pages and laptops.
  renderScale: Math.min(window.devicePixelRatio || 1, 1.18),

  // Visual tuning. Increase strength slightly if the distortion is too weak.
  // Distortion amplitude in physical pixels. Stronger than before so
  // text/background deformation remains visible after the overlay replaces
  // the original pixels inside the ripple band.
  strength: 18,

  // Shadow is no longer a fixed grey. It is computed from the local
  // background colour at every pixel, then darkened according to wave slope.
  shadowStrength: 0.44,

  // Smaller click ripple: the previous maximum was 520 px. This is almost
  // exactly one third, as requested.
  duration: 1500,
  maxRadius: 175,
  wavelength: 17,
  ringWidth: 27,
  speed: 190,
  tailRings: 3
};

let snapshotCanvas = null;
let snapshotCtx = null;
let snapshotKey = "";
let isCapturing = false;
let captureQueued = false;
let ripples = [];
let animationRunning = false;
let resizeTimer = 0;
let scrollTimer = 0;

function viewportKey() {
  return [
    Math.round(window.innerWidth),
    Math.round(window.innerHeight),
    Math.round(window.scrollX),
    Math.round(window.scrollY),
    document.documentElement.lang || "ja"
  ].join(":");
}

function resizeWaterCanvas() {
  if (!waterCanvas || !waterCtx) return;
  const s = waterSettings.renderScale;
  const w = Math.max(1, Math.round(window.innerWidth * s));
  const h = Math.max(1, Math.round(window.innerHeight * s));
  if (waterCanvas.width !== w || waterCanvas.height !== h) {
    waterCanvas.width = w;
    waterCanvas.height = h;
    waterCanvas.style.width = `${window.innerWidth}px`;
    waterCanvas.style.height = `${window.innerHeight}px`;
  }
}

function markSnapshotStale() {
  snapshotKey = "";
}

async function captureViewport() {
  if (!waterCanvas || !waterCtx) return null;

  // Avoid concurrent captures. If one is already running, remember that
  // another capture may be needed after it finishes.
  if (isCapturing) {
    captureQueued = true;
    return snapshotCanvas;
  }

  if (!window.html2canvas) {
    return null;
  }

  isCapturing = true;
  const currentKey = viewportKey();

  try {
    // Hide the overlay during capture so the screenshot is the real page only.
    const previousOpacity = waterCanvas.style.opacity;
    const previousDisplay = waterCanvas.style.display;
    waterCanvas.style.opacity = "0";
    waterCanvas.style.display = "none";

    // Let style changes apply before capture.
    await new Promise((resolve) => requestAnimationFrame(resolve));

    const captured = await window.html2canvas(document.body, {
      backgroundColor: null,
      scale: waterSettings.renderScale,
      width: window.innerWidth,
      height: window.innerHeight,
      x: window.scrollX,
      y: window.scrollY,
      scrollX: window.scrollX,
      scrollY: window.scrollY,
      windowWidth: document.documentElement.scrollWidth,
      windowHeight: document.documentElement.scrollHeight,
      ignoreElements: (el) => el === waterCanvas || el.classList?.contains("water-refraction"),
      logging: false,
      useCORS: true,
      allowTaint: false
    });

    waterCanvas.style.display = previousDisplay;
    waterCanvas.style.opacity = previousOpacity;

    snapshotCanvas = captured;
    snapshotCtx = captured.getContext("2d", { willReadFrequently: true });
    snapshotKey = currentKey;
    return snapshotCanvas;
  } catch (error) {
    // Restore overlay even if capture fails.
    waterCanvas.style.display = "";
    waterCanvas.style.opacity = "";
    console.warn("Water ripple capture failed; falling back to shadow-only waves.", error);
    return null;
  } finally {
    isCapturing = false;
    if (captureQueued) {
      captureQueued = false;
      window.setTimeout(() => captureViewport(), 120);
    }
  }
}

function clamp(value, min, max) {
  return Math.max(min, Math.min(max, value));
}

function smoothstep(edge0, edge1, x) {
  const t = clamp((x - edge0) / (edge1 - edge0), 0, 1);
  return t * t * (3 - 2 * t);
}

function sampleBilinear(data, width, height, x, y) {
  x = clamp(x, 0, width - 1.001);
  y = clamp(y, 0, height - 1.001);

  const x0 = Math.floor(x);
  const y0 = Math.floor(y);
  const x1 = Math.min(width - 1, x0 + 1);
  const y1 = Math.min(height - 1, y0 + 1);
  const tx = x - x0;
  const ty = y - y0;

  const i00 = (y0 * width + x0) * 4;
  const i10 = (y0 * width + x1) * 4;
  const i01 = (y1 * width + x0) * 4;
  const i11 = (y1 * width + x1) * 4;

  const out = [0, 0, 0, 0];
  for (let c = 0; c < 4; c += 1) {
    const a = data[i00 + c] * (1 - tx) + data[i10 + c] * tx;
    const b = data[i01 + c] * (1 - tx) + data[i11 + c] * tx;
    out[c] = a * (1 - ty) + b * ty;
  }
  return out;
}

function drawShadowOnly(ctx, ripple, age, progress) {
  const s = waterSettings.renderScale;
  const radius = Math.min(waterSettings.maxRadius, waterSettings.speed * age / 1000) * s;
  const cx = ripple.x * s;
  const cy = ripple.y * s;
  const alpha = (1 - progress) * 0.18;

  ctx.save();
  ctx.globalAlpha = alpha;
  ctx.lineWidth = Math.max(1, 1.4 * s);
  ctx.strokeStyle = "rgba(54, 71, 80, 0.42)";
  for (let i = 0; i < 4; i += 1) {
    const r = radius - i * waterSettings.wavelength * 1.1 * s;
    if (r > 4) {
      ctx.beginPath();
      ctx.arc(cx, cy, r, 0, Math.PI * 2);
      ctx.stroke();
    }
  }
  ctx.restore();
}

function drawRefractedRipple(ctx, ripple, now) {
  const s = waterSettings.renderScale;
  const age = now - ripple.start;
  const progress = clamp(age / waterSettings.duration, 0, 1);
  if (progress >= 1) return false;

  const W = waterCanvas.width;
  const H = waterCanvas.height;
  const cx = ripple.x * s;
  const cy = ripple.y * s;
  const maxR = waterSettings.maxRadius * s;
  const front = Math.min(maxR, (waterSettings.speed * age / 1000 + 16) * s);
  const tail = waterSettings.tailRings * waterSettings.wavelength * s;
  const boundR = Math.min(maxR, front + waterSettings.ringWidth * s);
  const innerR = Math.max(0, front - tail - waterSettings.ringWidth * s);

  const x0 = Math.max(0, Math.floor(cx - boundR - 3));
  const y0 = Math.max(0, Math.floor(cy - boundR - 3));
  const x1 = Math.min(W, Math.ceil(cx + boundR + 3));
  const y1 = Math.min(H, Math.ceil(cy + boundR + 3));
  const bw = x1 - x0;
  const bh = y1 - y0;
  if (bw <= 0 || bh <= 0) return true;

  const hasSnapshot = snapshotCanvas && snapshotCtx && snapshotCanvas.width >= W * 0.8 && snapshotCanvas.height >= H * 0.8;
  if (!hasSnapshot) {
    drawShadowOnly(ctx, ripple, age, progress);
    return true;
  }

  let srcImage;
  try {
    srcImage = snapshotCtx.getImageData(0, 0, snapshotCanvas.width, snapshotCanvas.height);
  } catch (error) {
    drawShadowOnly(ctx, ripple, age, progress);
    return true;
  }

  const src = srcImage.data;
  const srcW = snapshotCanvas.width;
  const srcH = snapshotCanvas.height;
  const dest = ctx.createImageData(bw, bh);
  const out = dest.data;

  const k = (Math.PI * 2) / (waterSettings.wavelength * s);
  const band = waterSettings.ringWidth * s;
  const fade = Math.pow(1 - progress, 1.7);
  const strength = waterSettings.strength * s * fade;
  const shadowStrength = waterSettings.shadowStrength * fade;
  const sourceScaleX = srcW / W;
  const sourceScaleY = srcH / H;

  for (let py = 0; py < bh; py += 1) {
    const y = y0 + py;
    const dy = y - cy;
    for (let px = 0; px < bw; px += 1) {
      const x = x0 + px;
      const dx = x - cx;
      const r = Math.sqrt(dx * dx + dy * dy);
      const outIndex = (py * bw + px) * 4;

      if (r < innerR || r > boundR || r < 1) {
        out[outIndex + 3] = 0;
        continue;
      }

      const phase = r - front;
      let wave = 0;
      let slope = 0;
      let opacity = 0;

      // Main outgoing capillary-like wave plus trailing smaller rings.
      for (let n = 0; n < waterSettings.tailRings; n += 1) {
        const center = -n * waterSettings.wavelength * s * 0.95;
        const local = phase - center;
        const envelope = Math.exp(-(local * local) / (2 * band * band));
        const ringDecay = Math.pow(0.64, n);
        const localPhase = local * k;
        wave += Math.sin(localPhase) * envelope * ringDecay;
        slope += Math.cos(localPhase) * envelope * ringDecay;
        opacity += envelope * ringDecay;
      }

      // Soft circular cutoff, so no object suddenly appears/disappears.
      const radialFadeIn = smoothstep(innerR, innerR + 18 * s, r);
      const radialFadeOut = 1 - smoothstep(boundR - 34 * s, boundR, r);
      const localAlpha = clamp(opacity * 0.72 * fade * radialFadeIn * radialFadeOut, 0, 0.82);
      if (localAlpha <= 0.004) {
        out[outIndex + 3] = 0;
        continue;
      }

      const nx = dx / r;
      const ny = dy / r;

      // Displacement follows the radial gradient of the wave height.
      const displacement = strength * slope;
      const swirl = 0.9 * s * wave * fade;
      const sampleX = (x - nx * displacement - ny * swirl) * sourceScaleX;
      const sampleY = (y - ny * displacement + nx * swirl) * sourceScaleY;
      const sampled = sampleBilinear(src, srcW, srcH, sampleX, sampleY);
      const base = sampleBilinear(src, srcW, srcH, x * sourceScaleX, y * sourceScaleY);

      // Per-pixel water shading:
      // The old version mixed every shadow with one fixed grey, which looked
      // artificial. Here the shadow colour is derived from the colour already
      // present behind the water at this exact coordinate. Dark bands therefore
      // become pale blue on the pale-blue background, deeper blue on blue cards,
      // and dark grey only over dark text.
      const shade = clamp(slope * shadowStrength, -0.55, 0.30);
      const baseLuma = 0.2126 * base[0] + 0.7152 * base[1] + 0.0722 * base[2];
      const greyFromBackground = [baseLuma, baseLuma, baseLuma];
      let rr = sampled[0];
      let gg = sampled[1];
      let bb = sampled[2];

      if (shade < 0) {
        const dark = -shade;
        // First remove a little saturation using the local luminance, then
        // darken multiplicatively. This gives a real shadow while preserving
        // the local colour family instead of painting an arbitrary grey ring.
        const desaturate = 0.16 + 0.10 * dark;
        rr = rr * (1 - desaturate) + greyFromBackground[0] * desaturate;
        gg = gg * (1 - desaturate) + greyFromBackground[1] * desaturate;
        bb = bb * (1 - desaturate) + greyFromBackground[2] * desaturate;
        rr *= (1 - 0.54 * dark);
        gg *= (1 - 0.54 * dark);
        bb *= (1 - 0.50 * dark);
      } else {
        const light = shade;
        // Highlights are also local: they gently move the refracted colour
        // toward a brighter version of the underlying background, not white.
        rr = rr * (1 - 0.22 * light) + Math.min(255, base[0] + (255 - base[0]) * 0.28) * (0.22 * light);
        gg = gg * (1 - 0.22 * light) + Math.min(255, base[1] + (255 - base[1]) * 0.28) * (0.22 * light);
        bb = bb * (1 - 0.22 * light) + Math.min(255, base[2] + (255 - base[2]) * 0.28) * (0.22 * light);
      }

      out[outIndex] = clamp(rr, 0, 255);
      out[outIndex + 1] = clamp(gg, 0, 255);
      out[outIndex + 2] = clamp(bb, 0, 255);

      // Use the canvas as a true pixel replacement inside the water band.
      // The previous semi-transparent overlay let the original unwarped text
      // remain visible underneath, so only the sticky tab looked distorted.
      // Since the replacement image is the same captured page, the soft edges
      // stay invisible except where real refraction/shading is present.
      const replacementAlpha = clamp((0.90 + 0.10 * opacity) * fade * radialFadeIn * radialFadeOut, 0, 0.98);
      out[outIndex + 3] = clamp(replacementAlpha * 255, 0, 250);
    }
  }

  ctx.putImageData(dest, x0, y0);
  return true;
}

function animateWater(now) {
  if (!waterCanvas || !waterCtx) return;
  resizeWaterCanvas();
  waterCtx.clearRect(0, 0, waterCanvas.width, waterCanvas.height);

  ripples = ripples.filter((ripple) => drawRefractedRipple(waterCtx, ripple, now));

  if (ripples.length > 0) {
    waterCanvas.classList.add("is-active");
    requestAnimationFrame(animateWater);
  } else {
    waterCanvas.classList.remove("is-active");
    animationRunning = false;
    waterCtx.clearRect(0, 0, waterCanvas.width, waterCanvas.height);
  }
}


function startWaterRippleAt(clientX, clientY) {
  ripples.push({
    x: clientX,
    y: clientY,
    start: performance.now()
  });

  if (!animationRunning) {
    animationRunning = true;
    requestAnimationFrame(animateWater);
  }
}

async function addWaterRipple(event) {
  if (!waterCanvas || !waterCtx) return;
  if (window.matchMedia("(prefers-reduced-motion: reduce)").matches) return;

  // Do not trigger water on modified clicks intended to open links in new tabs.
  if (event.metaKey || event.ctrlKey || event.shiftKey || event.altKey) return;

  resizeWaterCanvas();

  const key = viewportKey();
  if (!snapshotCanvas || snapshotKey !== key) {
    await captureViewport();
  }

  startWaterRippleAt(event.clientX, event.clientY);
}

// Keep a reasonably fresh snapshot. This makes ordinary clicks immediate,
// while still allowing scroll/language changes to be reflected.
function scheduleSnapshot(delay = 260) {
  markSnapshotStale();
  window.clearTimeout(scrollTimer);
  scrollTimer = window.setTimeout(() => {
    if (document.visibilityState === "visible") captureViewport();
  }, delay);
}

resizeWaterCanvas();
window.addEventListener("load", () => {
  window.setTimeout(async () => {
    await captureViewport();
    // A gentle opening ripple, inspired by sites that begin with a water-surface motion.
    startWaterRippleAt(window.innerWidth * 0.5, window.innerHeight * 0.43);
  }, 520);
});
window.addEventListener("resize", () => {
  window.clearTimeout(resizeTimer);
  resizeTimer = window.setTimeout(() => {
    resizeWaterCanvas();
    scheduleSnapshot(220);
  }, 160);
});
window.addEventListener("scroll", () => scheduleSnapshot(180), { passive: true });
document.addEventListener("visibilitychange", () => {
  if (document.visibilityState === "visible") scheduleSnapshot(180);
});

// Update the snapshot after language/reveal changes have altered the text.
buttons.forEach((button) => {
  button.addEventListener("click", () => window.setTimeout(() => scheduleSnapshot(80), 90));
});

// Reveal animations change the page appearance, so refresh the snapshot after
// the initial entrance animations are likely finished.
window.setTimeout(() => scheduleSnapshot(120), 1200);

document.addEventListener("click", addWaterRipple, { passive: true });
