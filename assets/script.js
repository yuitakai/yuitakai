const supportedLangs = Object.keys(translations);
const html = document.documentElement;
const buttons = document.querySelectorAll(".lang-button");
const nodes = document.querySelectorAll("[data-i18n]");

function setLanguage(lang) {
  const next = supportedLangs.includes(lang) ? lang : "ja";
  html.lang = next;
  localStorage.setItem("site-lang", next);

  nodes.forEach((node) => {
    const key = node.dataset.i18n;
    if (translations[next][key]) node.textContent = translations[next][key];
  });

  buttons.forEach((button) => {
    button.classList.toggle("is-active", button.dataset.lang === next);
  });
}

buttons.forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.lang));
});

const savedLang = localStorage.getItem("site-lang");
const browserLang = navigator.language?.slice(0, 2);
setLanguage(savedLang || (supportedLangs.includes(browserLang) ? browserLang : "ja"));

