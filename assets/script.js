/* ============================================================
   Physical water refraction ripple v11 — mobile local-crop coordinate fix

   Why this version is different:
   - It does NOT rely on a cached full-page or full-viewport screenshot.
   - Every tap captures only a local square around the tapped DOCUMENT point.
   - The ripple is drawn at the tapped SCREEN point, while the distorted pixels
     are sampled around the tapped DOCUMENT point.
   - This separates the two coordinate systems that often diverge on mobile
     after scrolling or after the address bar collapses.

   This design avoids the mobile scroll bug where the displayed ripple position
   was correct but the pixels used for refraction came from the wrong place.
   ============================================================ */

const waterCanvas = document.getElementById("waterRefraction");
const waterCtx = waterCanvas?.getContext("2d", { alpha: true, willReadFrequently: true });

const waterSettings = {
  renderScale: Math.min(window.devicePixelRatio || 1, 1.35),

  strength: 28,
  tangentialStrength: 1.05,
  shadowStrength: 0.22,
  highlightStrength: 0.045,

  duration: 1450,
  maxRadius: 175,
  wavelength: 15,
  ringSigma: 8.5,
  tailRings: 5,

  // Extra area captured around the maximum ripple radius. This prevents the
  // distorted samples from reaching outside the local screenshot.
  capturePadding: 76,

  visibilityCutoff: 0.018,
  maxActiveRipples: 3,

  // Mobile tap handling. If the finger moved more than this, it was a scroll,
  // not a tap ripple.
  maxTapMove: 14,
  maxTapTime: 760
};

