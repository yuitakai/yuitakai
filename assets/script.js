const supportedLangs = Object.keys(translations);
const html = document.documentElement;
const buttons = document.querySelectorAll(".lang-button");
const nodes = document.querySelectorAll("[data-i18n]");

function setLanguage(lang) {
  const next = supportedLangs.includes(lang) ? lang : "ja";
  html.lang = next;
  localStorage.setItem("site-lang", next);

  nodes.forEach((node) => {
    const key = node.dataset.i18n;
    if (translations[next][key]) node.textContent = translations[next][key];
  });

  buttons.forEach((button) => {
    button.classList.toggle("is-active", button.dataset.lang === next);
  });
}

buttons.forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.lang));
});

const savedLang = localStorage.getItem("site-lang");
const browserLang = navigator.language?.slice(0, 2);
setLanguage(savedLang || (supportedLangs.includes(browserLang) ? browserLang : "ja"));

document.getElementById("year").textContent = new Date().getFullYear();

const revealObserver = new IntersectionObserver((entries) => {
  entries.forEach((entry) => {
    if (entry.isIntersecting) {
      entry.target.classList.add("is-visible");
      revealObserver.unobserve(entry.target);
    }
  });
}, { threshold: 0.12 });

document.querySelectorAll(".reveal").forEach((el) => revealObserver.observe(el));

function createRipple(event) {
  if (window.matchMedia("(prefers-reduced-motion: reduce)").matches) return;

  const ripple = document.createElement("span");
  ripple.className = "ripple";
  ripple.style.left = `${event.clientX}px`;
  ripple.style.top = `${event.clientY}px`;
  document.body.appendChild(ripple);
  window.setTimeout(() => ripple.remove(), 920);
}

document.addEventListener("click", createRipple);
