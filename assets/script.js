const revealObserver = new IntersectionObserver((entries) => {
  entries.forEach((entry) => {
    if (entry.isIntersecting) {
      entry.target.classList.add("is-visible");
      revealObserver.unobserve(entry.target);
    }
  });
}, { threshold: 0.12 });

document.querySelectorAll(".reveal").forEach((el) => revealObserver.observe(el));



// -----------------------------------------------------------------------------
// Reliable refractive water ripple
// -----------------------------------------------------------------------------
// Design goal:
// - The original page is never transformed or filtered.
// - The ripple is created by masked duplicate layers of the page.
// - Only the annular bands around the click are visible, and those bands are
//   slightly scaled/shifted around the click point. This makes the real text,
//   cards, and images appear refracted without drawing white rings.
// - The effect is robust on GitHub Pages because it uses ordinary DOM cloning,
//   CSS transforms, and CSS masks. SVG displacement is optional/subtle only.

const siteContent = document.getElementById("siteContent");
const waterLayer = document.getElementById("waterLayer");
const reduceMotion = window.matchMedia("(prefers-reduced-motion: reduce)");

if (siteContent && waterLayer && !reduceMotion.matches) {
  const activeRipples = [];
  const MAX_ACTIVE_RIPPLES = 3;
  const RING_COUNT = 8;
  const RIPPLE_DURATION = 2350;
  let rafId = null;

  const maskSupported =
    CSS.supports("mask-image", "radial-gradient(circle, black, transparent)") ||
    CSS.supports("-webkit-mask-image", "radial-gradient(circle, black, transparent)");

  const clamp = (value, min, max) => Math.max(min, Math.min(max, value));
  const lerp = (a, b, t) => a + (b - a) * t;

  function easeOutCubic(t) {
    return 1 - Math.pow(1 - t, 3);
  }

  function easeInOutSine(t) {
    return 0.5 - 0.5 * Math.cos(Math.PI * t);
  }

  function stripCloneForAccessibility(root) {
    root.setAttribute("aria-hidden", "true");
    root.querySelectorAll("[id]").forEach((el) => el.removeAttribute("id"));
    root.querySelectorAll("a, button, input, textarea, select").forEach((el) => {
      el.setAttribute("tabindex", "-1");
      el.setAttribute("aria-hidden", "true");
    });
  }

  function makeContentClone() {
    const clone = siteContent.cloneNode(true);
    clone.classList.add("water-ripple__content");
    stripCloneForAccessibility(clone);
    return clone;
  }

  function makeBand(className, strength, direction) {
    const band = document.createElement("div");
    band.className = className;
    band.style.setProperty("--strength", String(strength));
    band.style.setProperty("--direction", String(direction));

    const clone = makeContentClone();
    band.appendChild(clone);
    return { band, clone, strength, direction };
  }

  function createRipple(clientX, clientY) {
    if (!maskSupported) return;

    const container = document.createElement("div");
    container.className = "water-ripple";
    container.style.setProperty("--x", `${clientX}px`);
    container.style.setProperty("--y", `${clientY}px`);

    const bands = [];

    // A broad inner lens gives the feeling that the surface itself briefly dips.
    bands.push(makeBand("water-ripple__band water-ripple__lens", 0.48, 1));

    // Multiple annular bands with alternating magnification produce real-looking
    // wave crests/troughs. They show displaced copies of the page, not a drawn line.
    for (let i = 0; i < RING_COUNT; i += 1) {
      const direction = i % 2 === 0 ? 1 : -1;
      const strength = 1 - i / (RING_COUNT + 1);
      bands.push(makeBand("water-ripple__band water-ripple__ring", strength, direction));
    }

    for (const item of bands) container.appendChild(item.band);
    waterLayer.appendChild(container);

    const ripple = {
      x: clientX,
      y: clientY,
      start: performance.now(),
      duration: RIPPLE_DURATION,
      container,
      bands,
      seed: Math.random() * Math.PI * 2,
    };

    activeRipples.push(ripple);

    while (activeRipples.length > MAX_ACTIVE_RIPPLES) {
      const old = activeRipples.shift();
      old.container.remove();
    }

    if (!rafId) rafId = requestAnimationFrame(animateRipples);
  }

  function setMask(element, value) {
    element.style.maskImage = value;
    element.style.webkitMaskImage = value;
    element.style.maskRepeat = "no-repeat";
    element.style.webkitMaskRepeat = "no-repeat";
    element.style.maskSize = "100% 100%";
    element.style.webkitMaskSize = "100% 100%";
  }

  function makeSoftDiskMask(x, y, radius, alpha) {
    const r0 = Math.max(0, radius * 0.22);
    const r1 = Math.max(r0 + 1, radius * 0.64);
    const r2 = Math.max(r1 + 1, radius);
    return `radial-gradient(circle at ${x.toFixed(1)}px ${y.toFixed(1)}px, rgba(0,0,0,${(0.32 * alpha).toFixed(3)}) 0px, rgba(0,0,0,${(0.22 * alpha).toFixed(3)}) ${r0.toFixed(1)}px, rgba(0,0,0,${(0.12 * alpha).toFixed(3)}) ${r1.toFixed(1)}px, transparent ${r2.toFixed(1)}px)`;
  }

  function makeAnnulusMask(x, y, radius, width, feather, alpha) {
    const innerFeather = Math.max(0, radius - width - feather);
    const inner = Math.max(0, radius - width);
    const coreIn = Math.max(0, radius - width * 0.38);
    const coreOut = radius + width * 0.38;
    const outer = radius + width;
    const outerFeather = radius + width + feather;

    return `radial-gradient(circle at ${x.toFixed(1)}px ${y.toFixed(1)}px,
      transparent 0px,
      transparent ${innerFeather.toFixed(1)}px,
      rgba(0,0,0,0) ${inner.toFixed(1)}px,
      rgba(0,0,0,${(0.62 * alpha).toFixed(3)}) ${coreIn.toFixed(1)}px,
      rgba(0,0,0,${(0.70 * alpha).toFixed(3)}) ${radius.toFixed(1)}px,
      rgba(0,0,0,${(0.50 * alpha).toFixed(3)}) ${coreOut.toFixed(1)}px,
      rgba(0,0,0,0) ${outer.toFixed(1)}px,
      transparent ${outerFeather.toFixed(1)}px,
      transparent 2200px)`;
  }

  function alignClone(clone, x, y, scale, dx, dy) {
    const scrollY = window.scrollY || window.pageYOffset || 0;
    clone.style.top = `${-scrollY}px`;
    clone.style.transformOrigin = `${x}px ${y + scrollY}px`;
    clone.style.transform = `translate3d(${dx.toFixed(2)}px, ${dy.toFixed(2)}px, 0) scale(${scale.toFixed(5)})`;
  }

  function updateRipple(ripple, now) {
    const t = clamp((now - ripple.start) / ripple.duration, 0, 1);
    const expansion = easeOutCubic(t);
    const fade = Math.pow(1 - t, 1.35);
    const breath = Math.sin(t * Math.PI * 8.5 + ripple.seed);
    const viewport = Math.hypot(window.innerWidth, window.innerHeight);
    const maxRadius = Math.min(viewport * 0.58, 720);

    ripple.container.style.opacity = String(clamp(fade * 1.08, 0, 1));

    ripple.bands.forEach((item, index) => {
      const isLens = index === 0;
      const localIndex = Math.max(0, index - 1);
      const phase = localIndex / Math.max(1, RING_COUNT - 1);
      const bandDelay = phase * 0.095;
      const bandT = clamp((t - bandDelay) / (1 - bandDelay), 0, 1);
      const bandExpansion = easeOutCubic(bandT);
      const bandFade = fade * Math.pow(1 - phase * 0.08, 1.5) * easeInOutSine(clamp(bandT * 1.25, 0, 1));

      if (isLens) {
        const lensRadius = lerp(42, 190, expansion) * (0.95 + 0.04 * breath);
        const lensScale = 1 + 0.010 * fade * (1 + 0.12 * breath);
        const lensDx = Math.cos(ripple.seed + 0.6) * 0.9 * fade;
        const lensDy = Math.sin(ripple.seed + 0.6) * 0.9 * fade;
        setMask(item.band, makeSoftDiskMask(ripple.x, ripple.y, lensRadius, bandFade));
        item.band.style.opacity = String(clamp(0.62 * bandFade, 0, 0.62));
        alignClone(item.clone, ripple.x, ripple.y, lensScale, lensDx, lensDy);
        return;
      }

      const radius = lerp(34 + localIndex * 18, maxRadius - localIndex * 22, bandExpansion);
      const waveWidth = lerp(9, 24, bandExpansion) * (1 + localIndex * 0.03);
      const feather = lerp(8, 25, bandExpansion);
      const alpha = clamp(bandFade * (0.95 - localIndex * 0.045), 0, 1);

      const direction = item.direction;
      const wave = Math.sin((1 - t) * Math.PI * 3.2 + localIndex * 0.9 + ripple.seed);
      const displacement = direction * (2.8 + 3.6 * item.strength) * fade * (0.75 + 0.25 * wave);
      const angle = ripple.seed + localIndex * 1.618 + t * 2.4;
      const dx = Math.cos(angle) * displacement;
      const dy = Math.sin(angle) * displacement;
      const scale = 1 + direction * (0.0065 + 0.0055 * item.strength) * fade;

      setMask(item.band, makeAnnulusMask(ripple.x, ripple.y, radius, waveWidth, feather, alpha));
      item.band.style.opacity = String(clamp(0.94 * alpha, 0, 0.94));
      alignClone(item.clone, ripple.x, ripple.y, scale, dx, dy);
    });

    return t < 1;
  }

  function animateRipples(now) {
    for (let i = activeRipples.length - 1; i >= 0; i -= 1) {
      const keep = updateRipple(activeRipples[i], now);
      if (!keep) {
        activeRipples[i].container.remove();
        activeRipples.splice(i, 1);
      }
    }

    if (activeRipples.length > 0) {
      rafId = requestAnimationFrame(animateRipples);
    } else {
      rafId = null;
    }
  }

  document.addEventListener("pointerdown", (event) => {
    if (event.button !== undefined && event.button !== 0) return;
    createRipple(event.clientX, event.clientY);
  }, { passive: true });
}
