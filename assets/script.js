/* ============================================================
   Physical water refraction ripple v6

   Design goals from the latest feedback:
   - No pale square behind the ripple.
   - No white drawn rings.
   - No appearing/disappearing cloned objects.
   - Background columns, text, and images are visually distorted because
     the ripple band is rendered from a full-page snapshot using exact
     document coordinates. This prevents unrelated text from being sampled.
   - Shadows are derived from the captured local background colour, and fade
     out smoothly before the ripple disappears.

   Implementation:
   1. html2canvas captures the current viewport to an offscreen canvas.
   2. For each active ripple, only pixels close to physical wave crests/troughs
      are redrawn on the transparent overlay canvas.
   3. Each redrawn pixel samples the captured page at a displaced coordinate,
      then receives local shading based on the wave slope.
   ============================================================ */

const waterCanvas = document.getElementById("waterRefraction");
const waterCtx = waterCanvas?.getContext("2d", { alpha: true, willReadFrequently: true });

const waterSettings = {
  renderScale: Math.min(window.devicePixelRatio || 1, 1.15),

  // Distortion and shading. Increase only a little if you want stronger ripples.
  strength: 18,
  tangentialStrength: 0.9,
  shadowStrength: 0.28,
  highlightStrength: 0.08,

  // Ripple size: kept to about one third of the earlier large ripple.
  duration: 1650,
  maxRadius: 175,
  wavelength: 15,
  ringSigma: 8.5,
  tailRings: 5,

  // Below this visible energy, the pixel is fully transparent. This is what
  // removes the pale rectangular bounding box.
  visibilityCutoff: 0.026,
  captureDebounce: 240
};

let snapshotCanvas = null;
let snapshotCtx = null;
let snapshotImageData = null;
let snapshotMeta = null;
let snapshotKey = "";
let isCapturing = false;
let captureQueued = false;
let ripples = [];
let animationRunning = false;
let resizeTimer = 0;
let snapshotTimer = 0;

function viewportKey() {
  const doc = document.documentElement;
  const body = document.body;
  const docW = Math.max(doc.scrollWidth, body?.scrollWidth || 0, window.innerWidth);
  const docH = Math.max(doc.scrollHeight, body?.scrollHeight || 0, window.innerHeight);
  return [
    Math.round(window.innerWidth),
    Math.round(window.innerHeight),
    Math.round(docW),
    Math.round(docH),
    document.documentElement.lang || "ja"
  ].join(":");
}

function resizeWaterCanvas() {
  if (!waterCanvas || !waterCtx) return;
  const s = waterSettings.renderScale;
  const w = Math.max(1, Math.round(window.innerWidth * s));
  const h = Math.max(1, Math.round(window.innerHeight * s));
  if (waterCanvas.width !== w || waterCanvas.height !== h) {
    waterCanvas.width = w;
    waterCanvas.height = h;
    waterCanvas.style.width = `${window.innerWidth}px`;
    waterCanvas.style.height = `${window.innerHeight}px`;
  }
}

function markSnapshotStale() {
  snapshotKey = "";
}

function clamp(value, min, max) {
  return Math.max(min, Math.min(max, value));
}

function smoothstep(edge0, edge1, x) {
  const t = clamp((x - edge0) / (edge1 - edge0), 0, 1);
  return t * t * (3 - 2 * t);
}

function easeOutCubic(t) {
  return 1 - Math.pow(1 - clamp(t, 0, 1), 3);
}

function sampleBilinear(data, width, height, x, y) {
  x = clamp(x, 0, width - 1.001);
  y = clamp(y, 0, height - 1.001);

  const x0 = Math.floor(x);
  const y0 = Math.floor(y);
  const x1 = Math.min(width - 1, x0 + 1);
  const y1 = Math.min(height - 1, y0 + 1);
  const tx = x - x0;
  const ty = y - y0;

  const i00 = (y0 * width + x0) * 4;
  const i10 = (y0 * width + x1) * 4;
  const i01 = (y1 * width + x0) * 4;
  const i11 = (y1 * width + x1) * 4;

  const out = [0, 0, 0, 255];
  for (let c = 0; c < 4; c += 1) {
    const a = data[i00 + c] * (1 - tx) + data[i10 + c] * tx;
    const b = data[i01 + c] * (1 - tx) + data[i11 + c] * tx;
    out[c] = a * (1 - ty) + b * ty;
  }
  return out;
}

async function captureViewport() {
  if (!waterCanvas || !waterCtx || !window.html2canvas) return null;

  if (isCapturing) {
    captureQueued = true;
    return snapshotCanvas;
  }

  isCapturing = true;
  const currentKey = viewportKey();

  try {
    // Hide only the water overlay during capture. The visible page itself is not modified.
    const previousVisibility = waterCanvas.style.visibility;
    waterCanvas.style.visibility = "hidden";
    await new Promise((resolve) => requestAnimationFrame(resolve));

    const doc = document.documentElement;
    const body = document.body;
    const docW = Math.max(doc.scrollWidth, body?.scrollWidth || 0, window.innerWidth);
    const docH = Math.max(doc.scrollHeight, body?.scrollHeight || 0, window.innerHeight);

    // Capture the whole document, not a cropped viewport. The previous version
    // cropped the viewport and then mapped coordinates back into that crop; on
    // some browsers this caused an offset, so text from another place could be
    // refracted at the click position. A full-page snapshot plus document
    // coordinates is slower but much more reliable and spatially correct.
    const captured = await window.html2canvas(document.documentElement, {
      backgroundColor: null,
      scale: waterSettings.renderScale,
      width: docW,
      height: docH,
      x: 0,
      y: 0,
      scrollX: 0,
      scrollY: 0,
      windowWidth: docW,
      windowHeight: docH,
      ignoreElements: (el) => el === waterCanvas || el.classList?.contains("water-refraction"),
      logging: false,
      useCORS: true,
      allowTaint: false,
      removeContainer: true
    });

    waterCanvas.style.visibility = previousVisibility;

    snapshotCanvas = captured;
    snapshotCtx = captured.getContext("2d", { willReadFrequently: true });
    snapshotImageData = snapshotCtx.getImageData(0, 0, captured.width, captured.height);
    snapshotMeta = {
      docWidth: docW,
      docHeight: docH,
      scaleX: captured.width / docW,
      scaleY: captured.height / docH
    };
    snapshotKey = currentKey;
    return snapshotCanvas;
  } catch (error) {
    waterCanvas.style.visibility = "";
    snapshotImageData = null;
    snapshotMeta = null;
    console.warn("Water ripple capture failed. Refraction needs html2canvas to capture the page.", error);
    return null;
  } finally {
    isCapturing = false;
    if (captureQueued) {
      captureQueued = false;
      window.setTimeout(() => captureViewport(), 120);
    }
  }
}

function computeLocalShade(sampled, base, slope, life) {
  const shade = clamp(slope * waterSettings.shadowStrength * life, -0.58, 0.36);
  let r = sampled[0];
  let g = sampled[1];
  let b = sampled[2];

  // Local background luminance, used as the local neutral shadow colour.
  const baseLuma = 0.2126 * base[0] + 0.7152 * base[1] + 0.0722 * base[2];

  if (shade < 0) {
    const dark = -shade;
    // Darken from the local background family, not from a fixed grey.
    // This makes the shadow blue-ish on blue areas, light grey on pale cards,
    // and darker only on text.
    const desat = 0.08 + 0.18 * dark;
    r = r * (1 - desat) + baseLuma * desat;
    g = g * (1 - desat) + baseLuma * desat;
    b = b * (1 - desat) + baseLuma * desat;
    r *= 1 - 0.62 * dark;
    g *= 1 - 0.60 * dark;
    b *= 1 - 0.56 * dark;
  } else {
    const light = shade * waterSettings.highlightStrength;
    // Highlights are intentionally weak and local. They are not white rings.
    r = r + (Math.max(base[0], r) - r) * 0.25 + (255 - r) * light;
    g = g + (Math.max(base[1], g) - g) * 0.25 + (255 - g) * light;
    b = b + (Math.max(base[2], b) - b) * 0.25 + (255 - b) * light;
  }

  return [clamp(r, 0, 255), clamp(g, 0, 255), clamp(b, 0, 255)];
}

function drawShadowOnly(ctx, ripple, now) {
  // Fallback only: no fixed white ring, no square. If html2canvas is blocked,
  // this gives a soft transparent local shadow rather than a broken effect.
  const s = waterSettings.renderScale;
  const age = now - ripple.start;
  const progress = clamp(age / waterSettings.duration, 0, 1);
  const life = Math.pow(1 - smoothstep(0.55, 1, progress), 2) * smoothstep(0, 0.08, progress);
  if (life < 0.03) return progress < 1;

  const front = waterSettings.maxRadius * easeOutCubic(progress) * s;
  const cx = ripple.x * s;
  const cy = ripple.y * s;
  ctx.save();
  ctx.globalAlpha = 0.16 * life;
  ctx.strokeStyle = "rgba(35, 55, 64, 0.32)";
  ctx.lineWidth = 1.2 * s;
  for (let n = 0; n < waterSettings.tailRings; n += 1) {
    const r = front - n * waterSettings.wavelength * 0.92 * s;
    if (r > 4) {
      ctx.beginPath();
      ctx.arc(cx, cy, r, 0, Math.PI * 2);
      ctx.stroke();
    }
  }
  ctx.restore();
  return progress < 1;
}

function drawRefractedRipple(ctx, ripple, now) {
  const s = waterSettings.renderScale;
  const age = now - ripple.start;
  const progress = clamp(age / waterSettings.duration, 0, 1);
  if (progress >= 1) return false;

  const life = Math.pow(1 - smoothstep(0.58, 1, progress), 2) * smoothstep(0, 0.055, progress);
  if (life < 0.018 && progress > 0.72) return false;

  const W = waterCanvas.width;
  const H = waterCanvas.height;
  const cx = ripple.x * s;
  const cy = ripple.y * s;
  const maxR = waterSettings.maxRadius * s;
  const front = maxR * easeOutCubic(progress);
  const tail = (waterSettings.tailRings - 1) * waterSettings.wavelength * 0.92 * s;
  const outerR = Math.min(maxR + 3 * waterSettings.ringSigma * s, front + 3.2 * waterSettings.ringSigma * s);
  const innerR = Math.max(0, front - tail - 3.2 * waterSettings.ringSigma * s);

  const x0 = Math.max(0, Math.floor(cx - outerR - 4));
  const y0 = Math.max(0, Math.floor(cy - outerR - 4));
  const x1 = Math.min(W, Math.ceil(cx + outerR + 4));
  const y1 = Math.min(H, Math.ceil(cy + outerR + 4));
  const bw = x1 - x0;
  const bh = y1 - y0;
  if (bw <= 0 || bh <= 0) return true;

  const hasSnapshot = snapshotImageData && snapshotMeta && snapshotCanvas && snapshotCanvas.width > 10 && snapshotCanvas.height > 10;
  if (!hasSnapshot) return drawShadowOnly(ctx, ripple, now);

  const src = snapshotImageData.data;
  const srcW = snapshotImageData.width;
  const srcH = snapshotImageData.height;
  const outImage = ctx.createImageData(bw, bh);
  const out = outImage.data;

  const wavelength = waterSettings.wavelength * s;
  const sigma = waterSettings.ringSigma * s;
  const k = (Math.PI * 2) / wavelength;
  const sourceScaleX = snapshotMeta.scaleX;
  const sourceScaleY = snapshotMeta.scaleY;
  const scrollX = window.scrollX || 0;
  const scrollY = window.scrollY || 0;

  for (let py = 0; py < bh; py += 1) {
    const y = y0 + py;
    const dy = y - cy;
    for (let px = 0; px < bw; px += 1) {
      const x = x0 + px;
      const dx = x - cx;
      const r = Math.sqrt(dx * dx + dy * dy);
      const outIndex = (py * bw + px) * 4;

      // Fully transparent outside the physical circular wave support.
      if (r < 1 || r < innerR || r > outerR) {
        out[outIndex + 3] = 0;
        continue;
      }

      let height = 0;
      let slope = 0;
      let energy = 0;

      // Several decaying capillary-like rings behind the leading front.
      for (let n = 0; n < waterSettings.tailRings; n += 1) {
        const ringR = front - n * wavelength * 0.92;
        if (ringR <= 0) continue;
        const local = r - ringR;
        const envelope = Math.exp(-(local * local) / (2 * sigma * sigma));
        const decay = Math.pow(0.68, n);
        const phase = local * k;
        height += Math.sin(phase) * envelope * decay;
        slope += Math.cos(phase) * envelope * decay;
        energy += envelope * decay;
      }

      // This is the key to removing the pale rectangle: pixels without wave
      // energy are strictly transparent, not faintly white.
      const visibility = energy * life;
      if (visibility < waterSettings.visibilityCutoff) {
        out[outIndex + 3] = 0;
        continue;
      }

      const nx = dx / r;
      const ny = dy / r;
      const alphaShape = smoothstep(waterSettings.visibilityCutoff, 0.22, visibility);
      const replacementAlpha = clamp(alphaShape * (0.985 - 0.08 * progress), 0, 0.985);

      // Refraction: sample the screenshot from a displaced coordinate. Because
      // the ripple pixels are almost opaque, text/cards/images under the wave
      // band are replaced by the distorted screenshot rather than showing the
      // original undistorted DOM underneath.
      // Keep the displacement local. Without this clamp, overlapping wave terms
      // can pull text from too far away, which looks like unrelated words are
      // appearing inside the ripple.
      const localSlope = clamp(slope, -1.15, 1.15);
      const localHeight = clamp(height, -1.2, 1.2);
      const radialDisplacement = waterSettings.strength * localSlope * life * alphaShape;
      const tangentialDisplacement = waterSettings.tangentialStrength * localHeight * life * alphaShape;

      // Convert viewport canvas pixels -> document CSS coordinates -> full-page
      // snapshot pixels. This is the key fix: the screenshot is a full document,
      // so sampling is tied to the actual click location, not to a possibly
      // mis-cropped viewport image.
      const docX = scrollX + x / s;
      const docY = scrollY + y / s;
      const sampleDocX = docX - nx * radialDisplacement - ny * tangentialDisplacement;
      const sampleDocY = docY - ny * radialDisplacement + nx * tangentialDisplacement;
      const sampleX = sampleDocX * sourceScaleX;
      const sampleY = sampleDocY * sourceScaleY;
      const originalX = docX * sourceScaleX;
      const originalY = docY * sourceScaleY;

      const sampled = sampleBilinear(src, srcW, srcH, sampleX, sampleY);
      const base = sampleBilinear(src, srcW, srcH, originalX, originalY);
      const shaded = computeLocalShade(sampled, base, slope, life * alphaShape);

      out[outIndex] = shaded[0];
      out[outIndex + 1] = shaded[1];
      out[outIndex + 2] = shaded[2];
      out[outIndex + 3] = Math.round(replacementAlpha * 255);
    }
  }

  ctx.putImageData(outImage, x0, y0);
  return true;
}

function animateWater(now) {
  if (!waterCanvas || !waterCtx) return;
  resizeWaterCanvas();
  waterCtx.clearRect(0, 0, waterCanvas.width, waterCanvas.height);

  ripples = ripples.filter((ripple) => drawRefractedRipple(waterCtx, ripple, now));

  if (ripples.length > 0) {
    requestAnimationFrame(animateWater);
  } else {
    animationRunning = false;
    waterCtx.clearRect(0, 0, waterCanvas.width, waterCanvas.height);
  }
}

function startWaterRippleAt(clientX, clientY) {
  ripples.push({
    x: clientX,
    y: clientY,
    start: performance.now()
  });

  if (!animationRunning) {
    animationRunning = true;
    requestAnimationFrame(animateWater);
  }
}

async function addWaterRipple(event) {
  if (!waterCanvas || !waterCtx) return;
  if (window.matchMedia("(prefers-reduced-motion: reduce)").matches) return;
  if (event.metaKey || event.ctrlKey || event.shiftKey || event.altKey) return;

  resizeWaterCanvas();

  const key = viewportKey();
  if (!snapshotCanvas || snapshotKey !== key) {
    await captureViewport();
  }

  startWaterRippleAt(event.clientX, event.clientY);
}

function scheduleSnapshot(delay = waterSettings.captureDebounce) {
  markSnapshotStale();
  window.clearTimeout(snapshotTimer);
  snapshotTimer = window.setTimeout(() => {
    if (document.visibilityState === "visible") captureViewport();
  }, delay);
}

