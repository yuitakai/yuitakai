const revealObserver = new IntersectionObserver((entries) => {
  entries.forEach((entry) => {
    if (entry.isIntersecting) {
      entry.target.classList.add("is-visible");
      revealObserver.unobserve(entry.target);
    }
  });
}, { threshold: 0.12 });

document.querySelectorAll(".reveal").forEach((el) => revealObserver.observe(el));

// -----------------------------------------------------------------------------
// Real refractive water interaction
// -----------------------------------------------------------------------------
// This version does not draw white rings above the page.
// Instead, it builds a grayscale displacement map on every animation frame and
// feeds it to an SVG feDisplacementMap filter. The filter is applied to the
// actual page content, so text, cards, and images bend locally as if they were
// reflected in a shallow puddle disturbed by a raindrop.

const siteContent = document.getElementById("siteContent");
const displacementMap = document.getElementById("waterDisplacementMap");
const displacementEffect = document.getElementById("waterDisplacementEffect");
const reduceMotion = window.matchMedia("(prefers-reduced-motion: reduce)");

if (siteContent && displacementMap && displacementEffect && !reduceMotion.matches) {
  const ripples = [];
  const mapCanvas = document.createElement("canvas");
  const mapCtx = mapCanvas.getContext("2d", { willReadFrequently: true });
  const xlinkNS = "http://www.w3.org/1999/xlink";

  let mapWidth = 0;
  let mapHeight = 0;
  let contentWidth = 0;
  let contentHeight = 0;
  let imageData = null;
  let neutralPixels = null;
  let animationId = null;

  function clamp(value, min, max) {
    return Math.max(min, Math.min(max, value));
  }

  function syncMapSize() {
    const rect = siteContent.getBoundingClientRect();
    contentWidth = Math.max(1, Math.round(rect.width));
    contentHeight = Math.max(1, Math.round(siteContent.scrollHeight));

    // Low-resolution map: enough for smooth refraction, light enough for GitHub Pages.
    const pixelSize = clamp(Math.ceil(Math.max(contentWidth, contentHeight) / 980), 3, 6);
    const nextWidth = clamp(Math.round(contentWidth / pixelSize), 220, 620);
    const nextHeight = clamp(Math.round(contentHeight / pixelSize), 260, 1500);

    if (nextWidth === mapWidth && nextHeight === mapHeight) return;

    mapWidth = nextWidth;
    mapHeight = nextHeight;
    mapCanvas.width = mapWidth;
    mapCanvas.height = mapHeight;
    imageData = mapCtx.createImageData(mapWidth, mapHeight);
    neutralPixels = new Uint8ClampedArray(imageData.data.length);

    for (let i = 0; i < neutralPixels.length; i += 4) {
      neutralPixels[i] = 128;     // R: horizontal neutral displacement
      neutralPixels[i + 1] = 128; // G: vertical neutral displacement
      neutralPixels[i + 2] = 128;
      neutralPixels[i + 3] = 255;
    }

    // Important: load a neutral map immediately. Without this, some browsers
    // briefly treat the displacement map as black, which moves the whole page
    // up-left. Neutral gray means exactly "no displacement".
    imageData.data.set(neutralPixels);
    updateSvgMap();
  }

  function setFilterScale(value) {
    displacementEffect.setAttribute("scale", String(value.toFixed(2)));
  }

  function clearWaterFilter() {
    setFilterScale(0);
    siteContent.classList.remove("is-refracting");
  }

  function updateSvgMap() {
    mapCtx.putImageData(imageData, 0, 0);
    const url = mapCanvas.toDataURL("image/png");
    displacementMap.setAttribute("href", url);
    displacementMap.setAttributeNS(xlinkNS, "href", url);
  }

  function addRipple(clientX, clientY, strength = 1) {
    syncMapSize();

    const rect = siteContent.getBoundingClientRect();
    ripples.push({
      x: clientX - rect.left,
      y: clientY - rect.top,
      start: performance.now(),
      strength,
      seed: Math.random() * Math.PI * 2,
    });

    while (ripples.length > 6) ripples.shift();

    if (!animationId) animationId = requestAnimationFrame(renderRefraction);
  }

  function renderRefraction(now) {
    syncMapSize();

    imageData.data.set(neutralPixels);
    const pixels = imageData.data;
    const liveRipples = [];
    let globalEnergy = 0;

    const sx = contentWidth / Math.max(1, mapWidth - 1);
    const sy = contentHeight / Math.max(1, mapHeight - 1);

    for (const ripple of ripples) {
      const age = (now - ripple.start) / 1000;
      const duration = 3.25;
      if (age >= duration) continue;
      liveRipples.push(ripple);

      const progress = age / duration;
      const decay = Math.pow(1 - progress, 1.65) * ripple.strength;
      globalEnergy = Math.max(globalEnergy, decay);

      // Top-view puddle behavior: a sharp first ring, followed by softer capillary waves.
      const front = 28 + 520 * (1 - Math.pow(1 - progress, 2.05));
      const wavelength = 38;
      const ringWidth = 122 + progress * 52;

      // Restrict calculations to the area where the ripple can matter.
      const maxRadius = front + ringWidth * 1.85;
      const minX = clamp(Math.floor((ripple.x - maxRadius) / sx), 0, mapWidth - 1);
      const maxX = clamp(Math.ceil((ripple.x + maxRadius) / sx), 0, mapWidth - 1);
      const minY = clamp(Math.floor((ripple.y - maxRadius) / sy), 0, mapHeight - 1);
      const maxY = clamp(Math.ceil((ripple.y + maxRadius) / sy), 0, mapHeight - 1);

      for (let my = minY; my <= maxY; my += 1) {
        const pageY = my * sy;
        const dy = pageY - ripple.y;
        for (let mx = minX; mx <= maxX; mx += 1) {
          const pageX = mx * sx;
          const dx = pageX - ripple.x;
          const r = Math.hypot(dx, dy);
          if (r < 0.001 || r > maxRadius) continue;

          const distanceFromFront = r - front;
          const envelope = Math.exp(-(distanceFromFront * distanceFromFront) / (2 * ringWidth * ringWidth));

          // Height-field derivative. The SVG filter uses the red/green channels as
          // local horizontal/vertical displacement, so the gradient creates refraction.
          const phase = (distanceFromFront / wavelength) * Math.PI * 2 - age * 5.2 + ripple.seed * 0.08;
          const slope = Math.cos(phase) * envelope * decay;

          // Secondary fine waves make the edge less computer-perfect.
          const fine = 0.34 * Math.cos(phase * 1.72 + r * 0.013) * envelope * decay;
          const radial = slope + fine;

          // A weak tangential component mimics small surface shear and avoids a flat CG ring.
          const tangent = 0.10 * Math.sin(phase * 0.71 + age * 3.0) * envelope * decay;
          const ux = dx / r;
          const uy = dy / r;

          const dispX = (ux * radial - uy * tangent) * 86;
          const dispY = (uy * radial + ux * tangent) * 86;

          const idx = (my * mapWidth + mx) * 4;
          pixels[idx] = clamp(pixels[idx] + dispX, 0, 255);
          pixels[idx + 1] = clamp(pixels[idx + 1] + dispY, 0, 255);
        }
      }
    }

    ripples.length = 0;
    ripples.push(...liveRipples);

    if (ripples.length > 0) {
      // Update the displacement image before increasing the filter scale.
      // This avoids a one-frame global jump in browsers that load feImage lazily.
      updateSvgMap();
      const filterScale = 54 * Math.pow(globalEnergy, 0.82);
      setFilterScale(filterScale);
      siteContent.classList.add("is-refracting");
      animationId = requestAnimationFrame(renderRefraction);
    } else {
      animationId = null;
      clearWaterFilter();
    }
  }

  window.addEventListener("resize", () => {
    syncMapSize();
  });

  document.addEventListener("pointerdown", (event) => {
    if (event.button !== undefined && event.button !== 0) return;
    addRipple(event.clientX, event.clientY, 1);
  });

  syncMapSize();
}
