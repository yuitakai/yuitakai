function waitForHtml2Canvas() {
  if (window.html2canvas) return Promise.resolve(window.html2canvas);
  if (html2canvasReady) return html2canvasReady;

  html2canvasReady = new Promise((resolve, reject) => {
    const started = performance.now();
    const check = () => {
      if (window.html2canvas) {
        resolve(window.html2canvas);
        return;
      }
      if (performance.now() - started > 4500) {
        reject(new Error("html2canvas was not loaded"));
        return;
      }
      window.setTimeout(check, 50);
    };
    check();
  });

  return html2canvasReady;
}

function getCanvasRect() {
  if (!waterCanvas) return { left: 0, top: 0, width: window.innerWidth, height: window.innerHeight };
  const rect = waterCanvas.getBoundingClientRect();
  return {
    left: rect.left,
    top: rect.top,
    width: Math.max(1, rect.width),
    height: Math.max(1, rect.height)
  };
}

function resizeWaterCanvas() {
  if (!waterCanvas || !waterCtx) return;

  // Use the actual rendered size of the fixed canvas. This is more reliable on
  // mobile than mixing innerHeight with visualViewport after URL-bar changes.
  const rect = getCanvasRect();
  const s = waterSettings.renderScale;
  const w = Math.max(1, Math.round(rect.width * s));
  const h = Math.max(1, Math.round(rect.height * s));

  if (waterCanvas.width !== w || waterCanvas.height !== h) {
    waterCanvas.width = w;
    waterCanvas.height = h;
  }
}

function clamp(value, min, max) {
  return Math.max(min, Math.min(max, value));
}

function smoothstep(edge0, edge1, x) {
  const t = clamp((x - edge0) / (edge1 - edge0), 0, 1);
  return t * t * (3 - 2 * t);
}

function easeOutCubic(t) {
  return 1 - Math.pow(1 - clamp(t, 0, 1), 3);
}

function sampleBilinear(data, width, height, x, y) {
  x = clamp(x, 0, width - 1.001);
  y = clamp(y, 0, height - 1.001);

  const x0 = Math.floor(x);
  const y0 = Math.floor(y);
  const x1 = Math.min(width - 1, x0 + 1);
  const y1 = Math.min(height - 1, y0 + 1);
  const tx = x - x0;
  const ty = y - y0;

  const i00 = (y0 * width + x0) * 4;
  const i10 = (y0 * width + x1) * 4;
  const i01 = (y1 * width + x0) * 4;
  const i11 = (y1 * width + x1) * 4;

  const out = [0, 0, 0, 255];
  for (let c = 0; c < 4; c += 1) {
    const a = data[i00 + c] * (1 - tx) + data[i10 + c] * tx;
    const b = data[i01 + c] * (1 - tx) + data[i11 + c] * tx;
    out[c] = a * (1 - ty) + b * ty;
  }
  return out;
}

function getEventClientPoint(event) {
  const source = event.changedTouches?.[0] || event.touches?.[0] || event;
  return {
    x: source.clientX,
    y: source.clientY
  };
}

function getEventPagePoint(event) {
  const source = event.changedTouches?.[0] || event.touches?.[0] || event;
  const client = getEventClientPoint(event);
  return {
    x: Number.isFinite(source.pageX) ? source.pageX : client.x + window.scrollX,
    y: Number.isFinite(source.pageY) ? source.pageY : client.y + window.scrollY
  };
}

function eventToRipplePoints(event) {
  if (!waterCanvas) return null;

  resizeWaterCanvas();
  const rect = getCanvasRect();
  const client = getEventClientPoint(event);
  const page = getEventPagePoint(event);

  const canvasX = client.x - rect.left;
  const canvasY = client.y - rect.top;

  if (!Number.isFinite(canvasX) || !Number.isFinite(canvasY)) return null;
  if (canvasX < -2 || canvasY < -2 || canvasX > rect.width + 2 || canvasY > rect.height + 2) return null;

  return {
    canvasX: clamp(canvasX, 0, rect.width),
    canvasY: clamp(canvasY, 0, rect.height),
    pageX: page.x,
    pageY: page.y,
    canvasRect: rect
  };
}

function documentSize() {
  const de = document.documentElement;
  const body = document.body;
  return {
    width: Math.max(
      de.scrollWidth,
      de.clientWidth,
      body ? body.scrollWidth : 0,
      body ? body.clientWidth : 0,
      window.innerWidth
    ),
    height: Math.max(
      de.scrollHeight,
      de.clientHeight,
      body ? body.scrollHeight : 0,
      body ? body.clientHeight : 0,
      window.innerHeight
    )
  };
}

async function captureLocalArea(points) {
  if (!waterCanvas || !waterCtx) return null;
  if (!window.html2canvas) await waitForHtml2Canvas();

  const s = waterSettings.renderScale;
  const doc = documentSize();
  const radius = waterSettings.maxRadius + waterSettings.capturePadding;

  let cropLeft = Math.floor(points.pageX - radius);
  let cropTop = Math.floor(points.pageY - radius);
  let cropWidth = Math.ceil(radius * 2);
  let cropHeight = Math.ceil(radius * 2);

  // Keep the local capture inside the document. If the tap is close to an edge,
  // the click point will no longer be exactly centered in the local screenshot,
  // so we store cropLeft/cropTop and use page coordinates for sampling.
  cropLeft = clamp(cropLeft, 0, Math.max(0, doc.width - 1));
  cropTop = clamp(cropTop, 0, Math.max(0, doc.height - 1));
  cropWidth = Math.max(1, Math.min(cropWidth, Math.ceil(doc.width - cropLeft)));
  cropHeight = Math.max(1, Math.min(cropHeight, Math.ceil(doc.height - cropTop)));

  const previousVisibility = waterCanvas.style.visibility;
  waterCanvas.style.visibility = "hidden";

  // One frame is enough to hide the canvas before html2canvas clones the DOM,
  // without waiting so long that mobile viewport bars can move again.
  await new Promise((resolve) => requestAnimationFrame(resolve));

  try {
    const captured = await window.html2canvas(document.documentElement, {
      backgroundColor: null,
      scale: s,
      x: cropLeft,
      y: cropTop,
      width: cropWidth,
      height: cropHeight,

      // For fixed/sticky elements, use the browser's current real scroll. The
      // crop itself is controlled by x/y above; this avoids the negative-scroll
      // compensation that caused mobile mismatches in earlier versions.
      scrollX: window.scrollX,
      scrollY: window.scrollY,
      windowWidth: Math.round(document.documentElement.clientWidth || window.innerWidth),
      windowHeight: Math.round(window.innerHeight),

      ignoreElements: (el) => el === waterCanvas || el.classList?.contains("water-refraction"),
      onclone: (clonedDocument) => {
        const clonedCanvas = clonedDocument.getElementById("waterRefraction");
        if (clonedCanvas) clonedCanvas.style.display = "none";
        clonedDocument.documentElement.style.scrollBehavior = "auto";
      },
      logging: false,
      useCORS: true,
      allowTaint: false,
      removeContainer: true
    });

    const ctx = captured.getContext("2d", { willReadFrequently: true });
    const imageData = ctx.getImageData(0, 0, captured.width, captured.height);

    return {
      canvas: captured,
      imageData,
      width: captured.width,
      height: captured.height,
      cropLeft,
      cropTop,
      cssWidth: cropWidth,
      cssHeight: cropHeight,
      scale: s
    };
  } catch (error) {
    console.warn("Water ripple local capture failed. Falling back to shadow-only ripple.", error);
    return null;
  } finally {
    waterCanvas.style.visibility = previousVisibility;
  }
}

function computeLocalShade(sampled, base, slope, life) {
  const shade = clamp(slope * waterSettings.shadowStrength * life, -0.50, 0.26);
  let r = sampled[0];
  let g = sampled[1];
  let b = sampled[2];

  const baseLuma = 0.2126 * base[0] + 0.7152 * base[1] + 0.0722 * base[2];

  if (shade < 0) {
    const dark = -shade;
    const desat = 0.04 + 0.12 * dark;
    r = r * (1 - desat) + baseLuma * desat;
    g = g * (1 - desat) + baseLuma * desat;
    b = b * (1 - desat) + baseLuma * desat;
    r *= 1 - 0.54 * dark;
    g *= 1 - 0.52 * dark;
    b *= 1 - 0.49 * dark;
  } else {
    const light = shade * waterSettings.highlightStrength;
    r = r + (255 - r) * light;
    g = g + (255 - g) * light;
    b = b + (255 - b) * light;
  }

  return [clamp(r, 0, 255), clamp(g, 0, 255), clamp(b, 0, 255)];
}

function drawShadowOnly(ctx, ripple, now) {
  const s = waterSettings.renderScale;
  const age = now - ripple.start;
  const progress = clamp(age / waterSettings.duration, 0, 1);
  const life = Math.pow(1 - smoothstep(0.5, 1, progress), 2) * smoothstep(0, 0.08, progress);
  if (life < 0.03) return progress < 1;

  const front = waterSettings.maxRadius * easeOutCubic(progress) * s;
  const cx = ripple.canvasX * s;
  const cy = ripple.canvasY * s;
  ctx.save();
  ctx.globalAlpha = 0.12 * life;
  ctx.strokeStyle = "rgba(26, 48, 58, 0.30)";
  ctx.lineWidth = 1.05 * s;
  for (let n = 0; n < waterSettings.tailRings; n += 1) {
    const r = front - n * waterSettings.wavelength * 0.92 * s;
    if (r > 4) {
      ctx.beginPath();
      ctx.arc(cx, cy, r, 0, Math.PI * 2);
      ctx.stroke();
    }
  }
  ctx.restore();
  return progress < 1;
}

function drawRefractedRipple(ctx, ripple, now) {
  const s = waterSettings.renderScale;
  const age = now - ripple.start;
  const progress = clamp(age / waterSettings.duration, 0, 1);
  if (progress >= 1) return false;

  const life = Math.pow(1 - smoothstep(0.58, 1, progress), 2) * smoothstep(0, 0.05, progress);
  if (life < 0.016 && progress > 0.72) return false;

  const W = waterCanvas.width;
  const H = waterCanvas.height;
  const cx = ripple.canvasX * s;
  const cy = ripple.canvasY * s;
  const maxR = waterSettings.maxRadius * s;
  const front = maxR * easeOutCubic(progress);
  const tail = (waterSettings.tailRings - 1) * waterSettings.wavelength * 0.92 * s;
  const outerR = Math.min(maxR + 3 * waterSettings.ringSigma * s, front + 3.4 * waterSettings.ringSigma * s);
  const innerR = Math.max(0, front - tail - 3.4 * waterSettings.ringSigma * s);

  const x0 = Math.max(0, Math.floor(cx - outerR - 4));
  const y0 = Math.max(0, Math.floor(cy - outerR - 4));
  const x1 = Math.min(W, Math.ceil(cx + outerR + 4));
  const y1 = Math.min(H, Math.ceil(cy + outerR + 4));
  const bw = x1 - x0;
  const bh = y1 - y0;
  if (bw <= 0 || bh <= 0) return true;

  if (!ripple.snapshot || !ripple.snapshot.imageData) {
    return drawShadowOnly(ctx, ripple, now);
  }

  const snap = ripple.snapshot;
  const src = snap.imageData.data;
  const srcW = snap.imageData.width;
  const srcH = snap.imageData.height;
  const outImage = ctx.createImageData(bw, bh);
  const out = outImage.data;

  const wavelength = waterSettings.wavelength * s;
  const sigma = waterSettings.ringSigma * s;
  const k = (Math.PI * 2) / wavelength;

  for (let py = 0; py < bh; py += 1) {
    const y = y0 + py;
    const dyDevice = y - cy;
    const dyCss = dyDevice / s;

    for (let px = 0; px < bw; px += 1) {
      const x = x0 + px;
      const dxDevice = x - cx;
      const dxCss = dxDevice / s;
      const r = Math.sqrt(dxDevice * dxDevice + dyDevice * dyDevice);
      const outIndex = (py * bw + px) * 4;

      if (r < 1 || r < innerR || r > outerR) {
        out[outIndex + 3] = 0;
        continue;
      }

      let height = 0;
      let slope = 0;
      let energy = 0;

      for (let n = 0; n < waterSettings.tailRings; n += 1) {
        const ringR = front - n * wavelength * 0.92;
        if (ringR <= 0) continue;
        const local = r - ringR;
        const envelope = Math.exp(-(local * local) / (2 * sigma * sigma));
        const decay = Math.pow(0.68, n);
        const phase = local * k;
        height += Math.sin(phase) * envelope * decay;
        slope += Math.cos(phase) * envelope * decay;
        energy += envelope * decay;
      }

      const visibility = energy * life;
      if (visibility < waterSettings.visibilityCutoff) {
        out[outIndex + 3] = 0;
        continue;
      }

      const nx = dxDevice / r;
      const ny = dyDevice / r;
      const alphaShape = smoothstep(waterSettings.visibilityCutoff, 0.18, visibility);
      const replacementAlpha = clamp(alphaShape * (0.99 - 0.12 * progress), 0, 0.99);

      const localSlope = clamp(slope, -1.2, 1.2);
      const localHeight = clamp(height, -1.25, 1.25);

      // Displacement is calculated in CSS pixels, then converted into the
      // local screenshot's pixel coordinates. Using pageX/pageY here, rather
      // than viewport coordinates, is the main mobile-scroll fix.
      const radialDisplacementCss = waterSettings.strength * localSlope * life * alphaShape;
      const tangentialDisplacementCss = waterSettings.tangentialStrength * localHeight * life * alphaShape;

      const basePageX = ripple.pageX + dxCss;
      const basePageY = ripple.pageY + dyCss;
      const samplePageX = basePageX - nx * radialDisplacementCss - ny * tangentialDisplacementCss;
      const samplePageY = basePageY - ny * radialDisplacementCss + nx * tangentialDisplacementCss;

      const sampleX = (samplePageX - snap.cropLeft) * snap.scale;
      const sampleY = (samplePageY - snap.cropTop) * snap.scale;
      const originalX = (basePageX - snap.cropLeft) * snap.scale;
      const originalY = (basePageY - snap.cropTop) * snap.scale;

      const sampled = sampleBilinear(src, srcW, srcH, sampleX, sampleY);
      const base = sampleBilinear(src, srcW, srcH, originalX, originalY);
      const shaded = computeLocalShade(sampled, base, slope, life * alphaShape);

      out[outIndex] = shaded[0];
      out[outIndex + 1] = shaded[1];
      out[outIndex + 2] = shaded[2];
      out[outIndex + 3] = Math.round(replacementAlpha * 255);
    }
  }

  ctx.putImageData(outImage, x0, y0);
  return true;
}

function animateWater(now) {
  if (!waterCanvas || !waterCtx) return;
  resizeWaterCanvas();
  waterCtx.clearRect(0, 0, waterCanvas.width, waterCanvas.height);

  const stillAlive = [];
  for (const ripple of ripples) {
    if (drawRefractedRipple(waterCtx, ripple, now)) stillAlive.push(ripple);
  }
  ripples = stillAlive;

  if (ripples.length > 0) {
    requestAnimationFrame(animateWater);
  } else {
    animationRunning = false;
    waterCtx.clearRect(0, 0, waterCanvas.width, waterCanvas.height);
  }
}

function startWaterRipple(ripple) {
  ripples.push(ripple);
  if (ripples.length > waterSettings.maxActiveRipples) {
    ripples = ripples.slice(-waterSettings.maxActiveRipples);
  }

  if (!animationRunning) {
    animationRunning = true;
    requestAnimationFrame(animateWater);
  }
}

async function addWaterRippleFromEvent(event) {
  if (!waterCanvas || !waterCtx) return;
  if (window.matchMedia("(prefers-reduced-motion: reduce)").matches) return;
  if (event.metaKey || event.ctrlKey || event.shiftKey || event.altKey) return;

  const points = eventToRipplePoints(event);
  if (!points) return;

  // Take the local screenshot before animation starts. The ripple position is
  // stored from the original event, so it remains at the tapped place even if
  // the browser performs small viewport updates while html2canvas is running.
  const snapshot = await captureLocalArea(points);

  startWaterRipple({
    canvasX: points.canvasX,
    canvasY: points.canvasY,
    pageX: points.pageX,
    pageY: points.pageY,
    snapshot,
    start: performance.now()
  });
}

function handlePointerDown(event) {
  if (!event.isPrimary && event.pointerType !== "mouse") return;
  const page = getEventPagePoint(event);
  const client = getEventClientPoint(event);
  pointerStart = {
    id: event.pointerId,
    clientX: client.x,
    clientY: client.y,
    pageX: page.x,
    pageY: page.y,
    time: performance.now()
  };
}

function handlePointerUp(event) {
  if (!pointerStart) return;
  if (pointerStart.id !== event.pointerId) return;

  const client = getEventClientPoint(event);
  const dx = client.x - pointerStart.clientX;
  const dy = client.y - pointerStart.clientY;
  const distance = Math.sqrt(dx * dx + dy * dy);
  const elapsed = performance.now() - pointerStart.time;
  pointerStart = null;

  if (distance > waterSettings.maxTapMove || elapsed > waterSettings.maxTapTime) {
    return;
  }

  lastPointerRippleAt = performance.now();
  addWaterRippleFromEvent(event);
}

function handlePointerCancel() {
  pointerStart = null;
}

function handleClickFallback(event) {
  // Avoid double-ripples on browsers that fire both pointerup and click.
  if (performance.now() - lastPointerRippleAt < 700) return;
  addWaterRippleFromEvent(event);
}

