window.addEventListener("load", () => {
  window.setTimeout(async () => {
    // The opening ripple uses a local capture at the center of the visible page.
    const rect = getCanvasRect();
    const fakeEvent = {
      clientX: rect.left + rect.width * 0.5,
      clientY: rect.top + rect.height * 0.42,
      pageX: window.scrollX + rect.left + rect.width * 0.5,
      pageY: window.scrollY + rect.top + rect.height * 0.42
    };
    await addWaterRippleFromEvent(fakeEvent);
  }, 760);
});

window.addEventListener("resize", () => {
  window.clearTimeout(resizeTimer);
  resizeTimer = window.setTimeout(() => {
    resizeWaterCanvas();
  }, 100);
});

if (window.visualViewport) {
  window.visualViewport.addEventListener("resize", () => {
    resizeWaterCanvas();
  }, { passive: true });

  window.visualViewport.addEventListener("scroll", () => {
    resizeWaterCanvas();
  }, { passive: true });
}

document.addEventListener("visibilitychange", () => {
  if (document.visibilityState === "visible") resizeWaterCanvas();
});

if (window.PointerEvent) {
  document.addEventListener("pointerdown", handlePointerDown, { passive: true });
  document.addEventListener("pointerup", handlePointerUp, { passive: true });
  document.addEventListener("pointercancel", handlePointerCancel, { passive: true });
} else {
  document.addEventListener("touchend", handleClickFallback, { passive: true });
  document.addEventListener("click", handleClickFallback, { passive: true });
}
